List<String> getJuzList() {
  return List.generate(30, (index) => "الجزء ${index + 1}");
}

String getJuzContent(int index) {
  if (quranAjza.containsKey(index + 1)) {
    return quranAjza[index + 1]! + "\n\n(يتم عرض النص القرآني الكامل هنا...)";
  }
  return "نص القرآن الكريم للجزء ${index + 1} يظهر هنا بشكل كامل ومنسق ومريح للعين مع إمكانية التكبير والتصغير...";
}
